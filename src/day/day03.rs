use crate::*;

simple_day!(|str|{
    let value = str.trim().parse::<i32>().unwrap();
    let root = (value as f64).sqrt();
    let mut filled = root.floor() as i32;
    if filled % 2 == 0 {
        filled -= 1;
    }
    let ring = (filled + 1) / 2;
    let ring_segment_length = ring * 2;
    let position_in_ring = value - filled * filled;
    let segment_offset = position_in_ring % ring_segment_length;
    let segment_center_offset = (segment_offset - ring_segment_length / 2).abs();

    let part1 = ring + segment_center_offset;
    let part2 = OEIS_TABLE.iter().find(|it|**it > value).unwrap();
    format!("Part1: {part1}, Part2: {part2}")
});

